#include <iostream>
#include <cmath> 

using std::cout;
using std::endl; 
int main() { 
	cout << "1 + 2 = " << 1+2 << endl; 
	cout << "1 - 2 = " << 1-2 << endl; 
	cout << "1 * 2 = " << 1*2 << endl; 
	cout << "1 / 2 = " << 1/2 << endl; 
	cout << "1 % 2 = " << 1%2 << endl;
	cout << "4 to the power of 3 = " << pow(4,3) << endl; 
	cout << "The square root of 9 = " << sqrt(9) << endl; 
 } 

